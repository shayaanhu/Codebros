def longest_common_subsequence(s, t):
    """
    Find the longest common subsequence of strings s and t.
    Returns one of the possible LCS strings.
    """
    m, n = len(s), len(t)
    
    # Create DP table where dp[i][j] stores the length of LCS of s[0...i-1] and t[0...j-1]
    dp = [[0] * (n + 1) for _ in range(m + 1)]
    
    # Fill the dp table
    for i in range(1, m + 1):
        for j in range(1, n + 1):
            if s[i-1] == t[j-1]:
                dp[i][j] = dp[i-1][j-1] + 1
            else:
                dp[i][j] = max(dp[i-1][j], dp[i][j-1])
    
    # Reconstruct the LCS
    lcs = []
    i, j = m, n
    while i > 0 and j > 0:
        if s[i-1] == t[j-1]:
            lcs.append(s[i-1])
            i -= 1
            j -= 1
        elif dp[i-1][j] >= dp[i][j-1]:
            i -= 1
        else:
            j -= 1
    
    # Return the LCS as a string (reverse it since we constructed it backwards)
    return ''.join(reversed(lcs))

